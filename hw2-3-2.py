# area and perimeter of a circle with a radius of 4.6
#area = pi radius squared
# perimeter is 2 pi r

r= 4.6
pi = 3.14

area = pi * r**2

perimeter = 2 * pi * r

print (" The area of the circle is " + str(area) + ". The perimeter of the circle is " + str(perimeter) + ".")