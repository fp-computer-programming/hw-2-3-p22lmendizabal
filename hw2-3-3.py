# Write a Python program that will display the area and perimeter of a rectangle that has a width of `13.9` and a height of `8.2`
w = 13.9
h = 8.2

area = w * h

perimeter = 2 * w + 2 * h

print(" THe area of the rectangle is " + str(area) + ". The perimeter of the rectangle is " + str(perimeter) + ".")